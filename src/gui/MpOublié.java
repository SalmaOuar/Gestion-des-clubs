    private void bnChercherLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bnChercherLoginActionPerformed
        // TODO add your handling code here:
        String login = bnMpoLogin.getText().trim();

        if (login.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Veuillez entrer un login.", "Erreur", JOptionPane.ERROR_MESSAGE);
            return;
        }

        Connection dbcn = (Connection) Connexion.getInstance().getCn();

        if (dbcn != null) {
            try {

                String query = "SELECT question_secrete FROM users WHERE login = ?";
                PreparedStatement st = dbcn.prepareStatement(query);
                st.setString(1, login);

                ResultSet rs = st.executeQuery();

                if (rs.next()) {
                    txtQuestionS.setText(rs.getString("question_secrete"));
                } else {
                    JOptionPane.showMessageDialog(this, "Login incorrect ou inexistant.", "Erreur", JOptionPane.ERROR_MESSAGE);
                }

                rs.close();
                st.close();

            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(this, "Erreur SQL : " + ex.getMessage(), "Erreur", JOptionPane.ERROR_MESSAGE);
                Logger.getLogger(MpOublié.class.getName()).log(Level.SEVERE, null, ex);
            }
        } else {
            JOptionPane.showMessageDialog(this, "Connexion à la base de données échouée.", "Erreur", JOptionPane.ERROR_MESSAGE);
        }

    }//GEN-LAST:event_bnChercherLoginActionPerformed

