    private void EnregistrerMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_EnregistrerMouseClicked
        // TODO add your handling code here:

        String login = bnMpoLogin.getText().trim();
        String question_secrete = txtQuestionS.getText().trim();
        String reponseSecrete = txtRepSecret.getText().trim();
        String newPassword = String.valueOf(txtPasswordMpO.getPassword()).trim();
        String confirmPassword = String.valueOf(txtCfPassword.getPassword()).trim();

        //if (email.isEmpty() || login.isEmpty() || nvpassword.isEmpty()||reponse_secrete.isEmpty() ) {
        //    JOptionPane.showMessageDialog(this, "Remplir tous les champs !", "Error", JOptionPane.ERROR_MESSAGE);
        //}else{}
        if (login.isEmpty() || reponseSecrete.isEmpty() || newPassword.isEmpty() || confirmPassword.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Veuillez remplir tous les champs.", "Erreur", JOptionPane.ERROR_MESSAGE);
            return;
        }

        if (!newPassword.equals(confirmPassword)) {
            JOptionPane.showMessageDialog(this, "Les mots de passe ne correspondent pas.", "Erreur", JOptionPane.ERROR_MESSAGE);
            return;
        }

        Connection dbcn = (Connection) Connexion.getInstance().getCn();

        if (dbcn != null) {
            try {

                String query = "SELECT reponse_secrete FROM users WHERE login = ?";
                PreparedStatement st = dbcn.prepareStatement(query);
                st.setString(1, login);
                ResultSet rs = st.executeQuery();

                if (rs.next()) {
                    String storedReponse = rs.getString("reponse_secrete");

                    if (!storedReponse.equalsIgnoreCase(reponseSecrete)) {
                        JOptionPane.showMessageDialog(this, "Réponse secrète incorrecte.", "Erreur", JOptionPane.ERROR_MESSAGE);
                        return;
                    }
                } else {
                    JOptionPane.showMessageDialog(this, "Utilisateur introuvable.", "Erreur", JOptionPane.ERROR_MESSAGE);
                    return;
                }

                String newSalt = PasswordUtils.generateSalt();
                String hashedPassword = PasswordUtils.hashPassword(newPassword, newSalt);

                String updateQuery = "UPDATE users SET password = ?, salt = ? WHERE login = ?";
                PreparedStatement updateSt = dbcn.prepareStatement(updateQuery);
                updateSt.setString(1, hashedPassword);
                updateSt.setString(2, newSalt);
                updateSt.setString(3, login);
                int rowsUpdated = updateSt.executeUpdate();

                if (rowsUpdated > 0) {
                    JOptionPane.showMessageDialog(this, "Mot de passe mis à jour avec succès !", "Succès", JOptionPane.INFORMATION_MESSAGE);
                    this.dispose();
                    new Login().setVisible(true);
                } else {
                    JOptionPane.showMessageDialog(this, "Échec de la mise à jour du mot de passe.", "Erreur", JOptionPane.ERROR_MESSAGE);
                }

                rs.close();
                st.close();
                updateSt.close();

            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(this, "Erreur SQL : " + ex.getMessage(), "Erreur", JOptionPane.ERROR_MESSAGE);
            }
        } else {
            JOptionPane.showMessageDialog(this, "Connexion à la base de données échouée.", "Erreur", JOptionPane.ERROR_MESSAGE);
        }


    }//GEN-LAST:event_EnregistrerMouseClicked

