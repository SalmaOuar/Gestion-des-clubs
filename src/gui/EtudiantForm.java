    void loadClub() {
        listClub.removeAllItems();
        for (Club c : cs.findAll()) {
            listClub.addItem(c);
        }

    }

    void load() {
        model.setRowCount(0);
        AdhesionService adhesionService = new AdhesionService();
        for (Etudiant e : es.findAll()) {

            Adhesion adhesion = adhesionService.findByIdEtudiant(e.getId());

            String clubNom = (adhesion != null) ? adhesion.getClub().getNom() : "Aucun club";

            model.addRow(new Object[]{
                e.getId(),
                e.getNom(),
                e.getPrenom(),
                e.getEmail(),
                clubNom
            });
        }
    }

