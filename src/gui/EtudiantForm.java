    private void bnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bnAddActionPerformed
        // TODO add your handling code here:
        String nom = txtNom.getText().toString();
        String prenom = txtPrenom.getText().toString();
        String email = txtEmail.getText().toString();
        Club club = (Club) listClub.getSelectedItem();
        Club clubSelectionne = (Club) listClub.getSelectedItem();
        if (es.create(new Etudiant(nom, prenom, email))) {
            JOptionPane.showMessageDialog(this, "Bien enregistré");

            Etudiant nouvelEtudiant = es.findByEmail(email);

            if (nouvelEtudiant != null && clubSelectionne != null) {
                Adhesion adhesion = new Adhesion(clubSelectionne, nouvelEtudiant, new Date());
                AdhesionService adhesionService = new AdhesionService();
                adhesionService.create(adhesion);
                System.out.println(" Adhésion créée pour " + nouvelEtudiant.getNom() + " au club " + clubSelectionne.getNom());
            }

            load();
        }
    }//GEN-LAST:event_bnAddActionPerformed

    private void bnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bnUpdateActionPerformed
        // TODO add your handling code here:
        int selectedRow = etudiantList.getSelectedRow();
        if (selectedRow != -1) {
            int id = Integer.parseInt(model.getValueAt(selectedRow, 0).toString());
            String nom = txtNom.getText();
            String prenom = txtPrenom.getText();
            String email = txtEmail.getText();
            Club clubSelectionne = (Club) listClub.getSelectedItem();

            Etudiant etudiant = new Etudiant(id, nom, prenom, email);
            es.update(etudiant);

            AdhesionService adhesionService = new AdhesionService();
            Adhesion ancienneAdhesion = adhesionService.findByIdEtudiant(id);

            if (ancienneAdhesion != null) {

                ancienneAdhesion.setClub(clubSelectionne);
                ancienneAdhesion.setDateAdhesion(new Date());
                adhesionService.update(ancienneAdhesion);
            } else {

                adhesionService.create(new Adhesion(clubSelectionne, etudiant, new Date()));
            }

            JOptionPane.showMessageDialog(this, "Étudiant modifié !");
            load();
        } else {
            JOptionPane.showMessageDialog(this, "Veuillez sélectionner un étudiant à modifier.");
        }
    }//GEN-LAST:event_bnUpdateActionPerformed

    private void bnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bnDeleteActionPerformed
        // TODO add your handling code here:
        int selectedRow = etudiantList.getSelectedRow();

        if (selectedRow != -1) {
            int id = Integer.parseInt(model.getValueAt(selectedRow, 0).toString());
            String email = model.getValueAt(selectedRow, 3).toString();

            int confirm = JOptionPane.showConfirmDialog(this, "Voulez-vous vraiment supprimer cet étudiant ?", "Confirmation", JOptionPane.YES_NO_OPTION);
            if (confirm == JOptionPane.YES_OPTION) {
                Etudiant etudiant = es.findById(id);
                AdhesionService adhesionService = new AdhesionService();
                Adhesion adhesion = adhesionService.findByIdEtudiant(id);

                if (adhesion != null) {
                    adhesionService.delete(adhesion);
                }

                if (es.delete(etudiant)) {
                    JOptionPane.showMessageDialog(this, "Étudiant supprimé avec succès.");
                    load();
                } else {
                    JOptionPane.showMessageDialog(this, "Erreur lors de la suppression.");
                }
            }
        } else {
            JOptionPane.showMessageDialog(this, "Veuillez sélectionner un étudiant à supprimer.");
        }
    }//GEN-LAST:event_bnDeleteActionPerformed

    private void etudiantListMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_etudiantListMouseClicked
        // TODO add your handling code here:
        int selectedRow = etudiantList.getSelectedRow();
        if (selectedRow != -1) {
            txtNom.setText(model.getValueAt(selectedRow, 1).toString());
            txtPrenom.setText(model.getValueAt(selectedRow, 2).toString());
            txtEmail.setText(model.getValueAt(selectedRow, 3).toString());

            String clubNom = model.getValueAt(selectedRow, 4).toString().trim();
            System.out.println("Nom à chercher : " + clubNom);
            for (int i = 0; i < listClub.getItemCount(); i++) {
                Club club = (Club) listClub.getItemAt(i);
                System.out.println("Item trouvé : " + club.getNom());
                if (club.getNom().equalsIgnoreCase(clubNom)) {
                    listClub.setSelectedIndex(i);
                    break;
                }
            }
        }
    }//GEN-LAST:event_etudiantListMouseClicked


